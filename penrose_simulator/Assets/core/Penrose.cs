using UnityEngine;

[RequireComponent(typeof(MeshFilter))]
[RequireComponent(typeof(MeshRenderer))]
public class Penrose : MonoBehaviour {

  private const int Total = 900;


  public Color bgColor = Color.gray;

  [Header("Display Size")]
  public float scale = 0.003f;
  public float gapScale = 0.9f;

  [HideInInspector]
  public Color[] buffer = new Color[Total]; // input buffer

  private readonly Vector3[] vertices = new Vector3[Total * 2 * 3];
  private readonly int[] triangles = new int[Total * 2 * 3];
  private readonly Color[] colors = new Color[Total * 2 * 3];
  
  private Mesh mesh;
  private MeshFilter meshFilter;
  private MeshRenderer meshRenderer;
  private Material material;

  private float bgBrightness;

  private void Awake() {
    meshFilter   = GetComponent<MeshFilter>();
    meshRenderer = GetComponent<MeshRenderer>();
    material =
      new Material(Shader.Find("Unlit/Penrose")) {hideFlags = HideFlags.HideAndDontSave, name = "PenMaterial"};
  }

  void Start() {
    var i = 0;
    var j = 0;
    
    // grab the geometry
    for(int n = 0; n < Geometry.Length; n += 6) {
      var a = new Vector3(Geometry[j++] * scale, Geometry[j++] * scale, 0f);
      var b = new Vector3(Geometry[j++] * scale, Geometry[j++] * scale, 0f);
      var c = new Vector3(Geometry[j++] * scale, Geometry[j++] * scale, 0f);

      var ab = b - a;
      var ac = c - a;
      
      if(Vector3.Cross(ab, ac).z > 0) {
        Vector3 x = c;
        c = a;
        a = x;
      }

      var middle = (a + c) / 2;
      a          = middle + (a - middle) * gapScale;
      b          = middle + (b - middle) * gapScale;
      c          = middle + (c - middle) * gapScale;

      vertices[i + 0] = a;
      vertices[i + 1] = b;
      vertices[i + 2] = c;

      triangles[i + 0] = i + 0;
      triangles[i + 1] = i + 1;
      triangles[i + 2] = i + 2;

      colors[i + 0] = bgColor;
      colors[i + 1] = bgColor;
      colors[i + 2] = bgColor;

      i += 3;
    }

    mesh = new Mesh {vertices = vertices, triangles = triangles, colors = colors};

    meshFilter.mesh       = mesh;
    meshRenderer.material = material;
    bgBrightness = bgColor.grayscale;

  }
  
  void Update() {
    UpdateVertexColors();
  }

  void UpdateVertexColors() {
    // color all the mesh vertices
    var x = 0;
    
    for(var i = 0; i < buffer.Length; i++) {
      
      var color = buffer[i];
      color = FadeColorToBgColor(color);
      
      // set the vertex color
      for(int j = 0; j < 6; j++) colors[x++] = color;
    }

    mesh.colors = colors;
  }

  private Color FadeColorToBgColor(Color color) {

    // Lerp between color and background
    color = Color.Lerp(bgColor, color, color.grayscale);

    // convert to HSV
    Color.RGBToHSV(color, out var h, out var s, out var v);

    // if current brightness is less than background bright, fix it
    if(v < bgBrightness) v = bgBrightness;

    // convert back to RGB
    color = Color.HSVToRGB(h, s, v);

    // return the new color
    return color;
  }

